#Arithmetic operators
num1 = 34
num2 = 10

print(num1 + num2)
print(num1 - num2)
print(num1 / num2)
print(num1 * num2)
print(num1 % num2)

#Comparison operators
print(num1 < num2)
print(num1 > num2)
print(num1 >= num2)
print(num1 <= num2)
print(num1 == num2) #Equal to
print(num1 != num2) #Not equal

#Assignement operators
number = 12
print(number)

number += 2
print(number)

b = 56
b -= 1
print(b)

#Logic operators - and, or, not
print( 34 < 10 and 50 > 20 )
print( 34 < 10 or 50 > 20 )
